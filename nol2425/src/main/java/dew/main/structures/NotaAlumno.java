package dew.main.structures;

public class NotaAlumno {
	String alumno;
	String nota;
	
	public String getAlumno() {
		return alumno;
	}
	public void setAlumno(String asignatura) {
		this.alumno = asignatura;
	}
	public String getNota() {
		return nota;
	}
	public void setNota(String nota) {
		this.nota = nota;
	}
}
